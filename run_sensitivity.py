def _read_csv(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(f"Expected CSV not found: {path}")
    return pd.read_csv(path)


def _write_tables_from_csvs(out_dir: Path) -> None:
    summary = _read_csv(out_dir / "sensitivity_summary.csv")
    interaction_summary = _read_csv(out_dir / "sensitivity_interaction_summary.csv")

    lines: list[str] = []
    lines.append("SENSITIVITY TABLES")
    lines.append("")
    lines.append("Aggregation note:")
    lines.append("- feasible rate uses the mean of the binary payload_feasible flag")
    lines.append("- continuous metrics should generally be interpreted through median and p10/p90")
    lines.append("")

    def add_table(title: str, df: pd.DataFrame):
        lines.append("=" * 120)
        lines.append(title)
        lines.append("")
        if df.empty:
            lines.append("(empty)")
            lines.append("")
            return
        lines.append(df.to_string(index=False))
        lines.append("")

    one_factor_cols = [
        "tag", "x_name", "x_value", "n_users",
        "payload_feasible_mean",
        "K_median", "U_mean_median", "U_max_median",
        "ent_edge_pct_median", "best_m_median",
        "payload_n_viol_T_median", "payload_T_over_sum_median",
        "runtime_total_s_median",
    ]
    one_factor_cols = [c for c in one_factor_cols if c in summary.columns]
    add_table("ONE-FACTOR SUMMARY", summary[one_factor_cols].copy() if not summary.empty else summary)

    inter_cols = [
        "tag", "n_users", "theta_3db_deg", "Ks_max",
        "payload_feasible_mean",
        "K_median", "U_mean_median", "U_max_median",
        "ent_edge_pct_median", "best_m_median",
        "runtime_total_s_median",
    ]
    inter_cols = [c for c in inter_cols if c in interaction_summary.columns]
    add_table("INTERACTION SUMMARY", interaction_summary[inter_cols].copy() if not interaction_summary.empty else interaction_summary)

    (out_dir / "sensitivity_tables.txt").write_text("\n".join(lines), encoding="utf-8")


def _to_numeric_sort_key(series: pd.Series) -> pd.Series:
    try:
        return pd.to_numeric(series)
    except Exception:
        return series.astype(str)


def _ordered_xlabels(g: pd.DataFrame) -> tuple[list[Any], dict[Any, int]]:
    tmp = g[["x_value"]].drop_duplicates().copy()
    tmp["_xsort"] = _to_numeric_sort_key(tmp["x_value"])
    tmp = tmp.sort_values("_xsort")
    labels = tmp["x_value"].tolist()
    pos_map = {v: i for i, v in enumerate(labels)}
    return labels, pos_map


def _plot_one_factor_metric(
    summary: pd.DataFrame,
    *,
    tag: str,
    metric_base: str,
    ylabel: str,
    out_dir: Path,
    use_mean: bool = False,
    y_log: bool = False,
) -> None:
    g = summary[summary["tag"] == tag].copy()
    if g.empty:
        return

    sweep_name = str(g["x_name"].iloc[0])
    labels, pos_map = _ordered_xlabels(g)

    if use_mean:
        center_col = f"{metric_base}_mean"
        low_col = None
        high_col = None
    else:
        center_col = f"{metric_base}_median"
        low_col = f"{metric_base}_p10"
        high_col = f"{metric_base}_p90"

    if center_col not in g.columns:
        return

    plt.figure(figsize=(7.2, 4.8))

    for n_users, gg in g.groupby("n_users"):
        gg = gg.copy()
        gg["_xsort"] = _to_numeric_sort_key(gg["x_value"])
        gg = gg.sort_values("_xsort")

        x = np.asarray([pos_map[v] for v in gg["x_value"]], dtype=float)
        y = pd.to_numeric(gg[center_col], errors="coerce").to_numpy(dtype=float)
        plt.plot(x, y, marker="o", label=f"n={int(n_users)}")

        if low_col is not None and high_col is not None and low_col in gg.columns and high_col in gg.columns:
            ylo = pd.to_numeric(gg[low_col], errors="coerce").to_numpy(dtype=float)
            yhi = pd.to_numeric(gg[high_col], errors="coerce").to_numpy(dtype=float)
            if np.isfinite(ylo).any() and np.isfinite(yhi).any():
                plt.fill_between(x, ylo, yhi, alpha=0.15)

    plt.xticks(range(len(labels)), [str(v) for v in labels])
    plt.xlabel(sweep_name)
    plt.ylabel(ylabel)
    if y_log:
        plt.yscale("log")
    plt.title(f"{ylabel} vs {sweep_name}")
    plt.grid(True, alpha=0.3)
    plt.legend()
    plt.tight_layout()
    fname = f"{tag.replace(':','_')}_{metric_base}.png"
    plt.savefig(out_dir / fname, dpi=160)
    plt.close()


def _annotate_heatmap(ax, data: np.ndarray) -> None:
    if data.size == 0:
        return
    for i in range(data.shape[0]):
        for j in range(data.shape[1]):
            v = data[i, j]
            if np.isfinite(v):
                if abs(v) >= 100 or float(v).is_integer():
                    txt = f"{v:.0f}"
                elif abs(v) >= 10:
                    txt = f"{v:.1f}"
                else:
                    txt = f"{v:.2f}"
                ax.text(j, i, txt, ha="center", va="center", fontsize=8)


