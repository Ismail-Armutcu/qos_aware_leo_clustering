def _plot_interaction_from_csvs(out_dir: Path) -> None:
    interaction_summary = _read_csv(out_dir / "sensitivity_interaction_summary.csv")
    if interaction_summary.empty:
        return

    metric_specs = [
        ("payload_feasible_mean", "Feasible rate"),
        ("K_median", "K"),
        ("U_mean_median", "U_mean"),
        ("U_max_median", "U_max"),
        ("ent_edge_pct_median", "Enterprise edge exposure (%)"),
        ("best_m_median", "Minimum feasible prefix"),
        ("runtime_total_s_median", "Runtime (s)"),
    ]

    for n_users, g in interaction_summary.groupby("n_users"):
        g = g.copy()
        g["theta_3db_deg"] = pd.to_numeric(g["theta_3db_deg"], errors="coerce")
        g["Ks_max"] = pd.to_numeric(g["Ks_max"], errors="coerce")
        g = g.sort_values(["theta_3db_deg", "Ks_max"])

        for metric, title_metric in metric_specs:
            if metric not in g.columns:
                continue
            pivot = g.pivot(index="theta_3db_deg", columns="Ks_max", values=metric)
            if pivot.empty:
                continue

            fig, ax = plt.subplots(figsize=(6.2, 5.0))
            im = ax.imshow(pivot.values, aspect="auto", origin="lower")
            ax.set_xticks(range(len(pivot.columns)))
            ax.set_xticklabels([str(int(c)) if float(c).is_integer() else str(c) for c in pivot.columns])
            ax.set_yticks(range(len(pivot.index)))
            ax.set_yticklabels([str(v) for v in pivot.index])
            ax.set_xlabel("Ks_max")
            ax.set_ylabel("theta_3db_deg")
            ax.set_title(f"{title_metric}, n_users={int(n_users)}")
            fig.colorbar(im, ax=ax)
            _annotate_heatmap(ax, pivot.values)
            plt.tight_layout()
            plt.savefig(out_dir / f"interaction_theta_x_Ksmax_{metric}_n{int(n_users)}.png", dpi=160)
            plt.close()


